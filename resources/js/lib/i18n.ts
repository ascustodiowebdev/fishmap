function interpolate(template: string, params?: Record<string, string | number | null | undefined>) {
    if (!params) {
        return template;
    }

    return Object.entries(params).reduce((result, [key, value]) => result.replaceAll(`{${key}}`, `${value ?? ''}`), template);
}

export function useTranslator() {
    const { locale = 'en' } = usePage<SharedData>().props;
    const currentLocale = (locale === 'pt' ? 'pt' : 'en') as Locale;

    return {
        locale: currentLocale,
        t: (key: keyof (typeof translations)['en'] | string, params?: Record<string, string | number | null | undefined>) => {
            const template = translations[currentLocale][key] ?? translations.en[key] ?? key;
            return interpolate(template, params);
        },
    };
}
